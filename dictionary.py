#dictionary

# this is how you make a dictionary
This_is_dictionary = {
    "name": "bob",
    "sport": "soccer",
    "Fav_colours": ["blue", "red"],
    "Bool_value": False,
    "tuple": (1,2,3)
}

# this is how you access a dictionary
print(This_is_dictionary["name"])

x = This_is_dictionary["Fav_colours"]
print(x)

value = This_is_dictionary["Bool_value"]
print(value)

print(This_is_dictionary["tuple"])

